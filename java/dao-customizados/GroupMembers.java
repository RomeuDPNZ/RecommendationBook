
package recBook;

public class GroupMembers {

	private Long id;
	private String name;

	public GroupMembers() {
	}

	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

}